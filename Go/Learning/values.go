package main

import "fmt"

func main() {
	fmt.Println("---- [TEXT] ----")
	fmt.Println("Hi " + "osamah")
	
	fmt.Println("---- [NUMBERS] -----")
	fmt.Println("5*5= ", 5*5)
	fmt.Println("5*6= ", 5*6)

	fmt.Println("---- [LOGC] ----")
	fmt.Println(true && true)
	fmt.Println(false || false)
	fmt.Println(!false)

}